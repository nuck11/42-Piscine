#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		ft_putchar(*str);
		str++;
	}
}

void	print_matriz(int arr[4][4])
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			ft_putchar(arr[i][j] + 48);
			if (j < 3)
			{
				ft_putchar(' ');
			}
			++j;
		}
		ft_putchar('\n');
		++i;
	}
}
